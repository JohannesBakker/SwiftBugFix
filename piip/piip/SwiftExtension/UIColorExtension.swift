import UIKit

extension UIColor {
    
    class func pp_mainTintColor() -> UIColor {
        return UIColor(red: 63 / 255, green: 161 / 255, blue: 72 / 255, alpha: 1)
    }
    
    class func pp_navigationBarColor() -> UIColor {
        return UIColor(red: 63 / 255, green: 161 / 255, blue: 72 / 255, alpha: 1)
    }
}
